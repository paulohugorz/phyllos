CREATE TABLE IF NOT EXISTS visual_references (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  slug TEXT NOT NULL UNIQUE,
  titulo TEXT NOT NULL,
  fonte TEXT NOT NULL,
  source_url TEXT NOT NULL,
  image_url TEXT,
  categoria TEXT NOT NULL DEFAULT 'athleisure',
  contexto_uso TEXT NOT NULL,
  cenario TEXT NOT NULL,
  pecas_chave TEXT,
  paleta TEXT,
  styling_notes TEXT,
  atributos_visuais TEXT,
  prompt_tokens TEXT,
  prompt_negativo TEXT,
  direitos_uso TEXT NOT NULL DEFAULT 'Referencia externa para curadoria. Nao copiar imagem para campanha ou produto final.',
  status TEXT NOT NULL DEFAULT 'prospectado',
  prioridade INTEGER NOT NULL DEFAULT 3,
  curador TEXT NOT NULL DEFAULT 'Codex',
  revisado_em TEXT NOT NULL DEFAULT (DATE('now')),
  criado_em DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS ix_visual_references_contexto_uso ON visual_references (contexto_uso);
CREATE INDEX IF NOT EXISTS ix_visual_references_cenario ON visual_references (cenario);
CREATE INDEX IF NOT EXISTS ix_visual_references_status ON visual_references (status);

CREATE TABLE IF NOT EXISTS peca_visual_references (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  peca_id INTEGER NOT NULL,
  visual_reference_id INTEGER NOT NULL,
  tipo_vinculo TEXT NOT NULL DEFAULT 'moodboard',
  observacoes TEXT,
  criado_em DATETIME DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (peca_id, visual_reference_id, tipo_vinculo),
  FOREIGN KEY (peca_id) REFERENCES pecas (id),
  FOREIGN KEY (visual_reference_id) REFERENCES visual_references (id)
);

CREATE INDEX IF NOT EXISTS ix_peca_visual_references_peca_id ON peca_visual_references (peca_id);
CREATE INDEX IF NOT EXISTS ix_peca_visual_references_visual_id ON peca_visual_references (visual_reference_id);

